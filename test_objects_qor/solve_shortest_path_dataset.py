#!/usr/bin/env python3
"""Graph-based solver for the shortest-path dataset.

This solver is inspired by the rotation graph used in `interactive_rotation.py`.
It builds a rotation graph from the initial object of each sample, then returns
all shortest action sequences from the initial object to the sample target.

The dataset may contain distractor targets generated by mirroring or safe cube
erasures. Those are expected to be unsolvable from the initial object, so the
solver reports an empty path list for them.
"""

from __future__ import annotations

import argparse
import copy
import functools
import json
import sys
from collections import defaultdict, deque
from pathlib import Path
from typing import Dict, Iterable, List, Tuple


HERE = Path(__file__).parent

REPO_ROOT = None
for parent in HERE.resolve().parents:
    if (parent / "visualize_cubes_qor.py").exists():
        REPO_ROOT = parent
        break

if REPO_ROOT is None:
    REPO_ROOT = HERE.parent

sys.path.insert(0, str(REPO_ROOT))

import visualize_cubes_qor as vcq


ALL_ROTATIONS = [
    "towards_up",
    "towards_down",
    "towards_left",
    "towards_right",
    "1q",
    "-1q",
]

FACE_ORDER = ["front", "back", "left", "right", "up", "down"]


def clone_faces(faces):
    return {face: copy.deepcopy(matrix) for face, matrix in faces.items()}


def serialize_faces(faces):
    parts = []
    for face in FACE_ORDER:
        matrix = faces.get(face)
        if matrix is None:
            parts.append("None")
        else:
            parts.append("|".join("".join(row) for row in matrix))
    return "\n".join(parts)


def full_cube_signature(faces):
    return tuple((face, tuple(tuple(row) for row in faces[face])) for face in FACE_ORDER)


def apply_rotation(faces, rotation):
    if rotation in {"1q", "-1q"}:
        return vcq.apply_in_plane_rotation(faces, rotation)
    return vcq.apply_rotation_via_prolog(faces, rotation)


def build_rotation_graph(start_faces, max_depth=3):
    """Build a rotation graph and shortest-path parent map from start_faces."""
    start_sig = full_cube_signature(start_faces)

    states = {start_sig: clone_faces(start_faces)}
    dist = {start_sig: 0}
    parents = defaultdict(list)  # node -> [(parent_node, rotation)]
    graph = defaultdict(list)    # node -> [(rotation, next_node)]

    queue = deque([start_sig])

    while queue:
        sig = queue.popleft()
        depth = dist[sig]

        if depth >= max_depth:
            continue

        faces = states[sig]

        for rotation in ALL_ROTATIONS:
            next_faces = apply_rotation(faces, rotation)
            next_sig = full_cube_signature(next_faces)
            next_depth = depth + 1

            graph[sig].append((rotation, next_sig))

            if next_sig not in states:
                states[next_sig] = next_faces

            if next_sig not in dist:
                dist[next_sig] = next_depth
                parents[next_sig].append((sig, rotation))
                queue.append(next_sig)
            elif next_depth == dist[next_sig]:
                parents[next_sig].append((sig, rotation))

    return {
        "start_sig": start_sig,
        "states": states,
        "dist": dist,
        "parents": parents,
        "graph": graph,
    }


def all_shortest_paths(graph_data, target_sig):
    """Return all shortest rotation sequences from the graph start to target_sig."""
    start_sig = graph_data["start_sig"]
    dist = graph_data["dist"]
    parents = graph_data["parents"]

    if target_sig not in dist:
        return []

    @functools.lru_cache(maxsize=None)
    def backtrack(sig):
        if sig == start_sig:
            return ((),)

        sequences = []

        for parent_sig, rotation in parents.get(sig, []):
            for prefix in backtrack(parent_sig):
                sequences.append(prefix + (rotation,))

        # Deduplicate while preserving order.
        unique = []
        seen = set()
        for seq in sequences:
            if seq not in seen:
                seen.add(seq)
                unique.append(seq)

        return tuple(unique)

    return [list(seq) for seq in backtrack(target_sig)]


def load_dataset(dataset_path):
    with open(dataset_path, "r", encoding="utf-8") as f:
        return json.load(f)


def solve_dataset(dataset_path, max_depth=3, limit=None):
    dataset = load_dataset(dataset_path)
    samples = dataset["samples"]

    if limit is not None:
        samples = samples[:limit]

    graph_cache = {}
    results = []

    for idx, sample in enumerate(samples):
        initial_object = sample["initial_object"]
        initial_faces = sample["initial_faces"]

        cache_key = (initial_object, serialize_faces(initial_faces))

        if cache_key not in graph_cache:
            graph_cache[cache_key] = build_rotation_graph(initial_faces, max_depth=max_depth)

        graph_data = graph_cache[cache_key]
        target_sig = full_cube_signature(sample["target_faces"])
        shortest_paths = all_shortest_paths(graph_data, target_sig)

        results.append({
            "sample_id": sample["sample_id"],
            "initial_object": initial_object,
            "sequence": sample["sequence"],
            "target_mode": sample["target_mode"],
            "target_is_distractor": sample["target_is_distractor"],
            "solvable": bool(shortest_paths),
            "shortest_path_count": len(shortest_paths),
            "shortest_path_length": len(shortest_paths[0]) if shortest_paths else None,
            "all_shortest_paths": shortest_paths,
            "graph_state_count": len(graph_data["states"]),
            "graph_depth_limit": max_depth,
        })

    summary = {
        "sample_count": len(results),
        "solvable_count": sum(1 for item in results if item["solvable"]),
        "unsolved_count": sum(1 for item in results if not item["solvable"]),
        "max_depth": max_depth,
        "initial_objects": sorted({item["initial_object"] for item in results}),
    }

    return {
        "summary": summary,
        "results": results,
    }


def write_json(path, payload):
    with open(path, "w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2)


def main():
    parser = argparse.ArgumentParser(description="Solve the shortest-path dataset using a rotation graph.")
    parser.add_argument(
        "--dataset",
        type=str,
        default=str(HERE / "shortest_path_dataset" / "dataset.json"),
        help="Path to dataset.json",
    )
    parser.add_argument(
        "--output",
        type=str,
        default=str(HERE / "shortest_path_dataset" / "solver_results.json"),
        help="Path to write solver results JSON",
    )
    parser.add_argument(
        "--max-depth",
        type=int,
        default=3,
        help="Graph expansion depth per initial object",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=None,
        help="Optional limit on the number of samples to solve",
    )
    args = parser.parse_args()

    solved = solve_dataset(args.dataset, max_depth=args.max_depth, limit=args.limit)
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    write_json(output_path, solved)

    print(json.dumps(solved["summary"], indent=2))
    print(f"Wrote solver results to {output_path}")


if __name__ == "__main__":
    main()