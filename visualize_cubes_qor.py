def get_cube_data(obj_name):
    """Query Prolog for all 6 faces of an object."""
    faces = {}
    face_names = ['front', 'back', 'left', 'right', 'up', 'down']

    for face in face_names:
        query = (
            f":- consult('{Q3D_PROLOG_FILE}'), "
            f"view({face}, {obj_name}, _, _, M), "
            f"write(M), halt.\n"
        )

        output = query_prolog(query)

        if output:
            faces[face] = parse_prolog_matrix(output)
        else:
            faces[face] = None

    return faces


def extract_faces_from_q3d(obj_name, q3d_path=None):
    """Extract faces directly from q3d-ex.pl."""
    if q3d_path is None:
        q3d_path = Path(__file__).parent / Q3D_PROLOG_FILE

    text = q3d_path.read_text(encoding='utf-8', errors='replace')
    faces = {}

    pattern = re.compile(
        r"view\(\s*"
        r"(front|back|left|right|up|down)"
        r"\s*,\s*"
        + re.escape(obj_name)
        + r"\s*,\s*\d+\s*,\s*\d+\s*,\s*(\[\[.*?\]\])\s*\)\.",
        re.DOTALL
    )

    for match in pattern.finditer(text):
        face = match.group(1)
        matrix_str = match.group(2)
        faces[face] = parse_prolog_matrix(matrix_str)

    return {
        face: faces.get(face, None)
        for face in ['front', 'back', 'left', 'right', 'up', 'down']
    }


