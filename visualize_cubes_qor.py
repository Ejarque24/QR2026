def query_prolog(query):
    """Execute a Prolog query and return stdout."""
    try:
        result = subprocess.run(
            ['swipl', '-q', '-t', 'halt'],
            input=query.encode(),
            capture_output=True,
            timeout=10,
            cwd=str(PROLOG_DIR)
        )

        stderr = result.stderr.decode().strip()
        if stderr:
            print("Prolog stderr:")
            print(stderr)

        return result.stdout.decode().strip()

    except Exception as e:
        print(f"Prolog error: {e}")
        return ""


def matrix_to_prolog(mat):
    """Convert Python matrix to Prolog list notation."""
    rows = []

    for row in mat:
        cells = []

        for cell in row:
            if isinstance(cell, str):
                if cell == '*':
                    cells.append("'*'")
                else:
                    cells.append(f"'{cell}'")
            else:
                cells.append(str(cell))

        rows.append('[' + ','.join(cells) + ']')

    return '[' + ','.join(rows) + ']'


def parse_prolog_matrix(prolog_str):
    """
    Parse Prolog matrix notation like:

    [[a,b,c],[d,e,f],[g,h,i]]

    into Python list of lists.
    """
    try:
        lines = [line.strip() for line in prolog_str.splitlines() if line.strip()]

        matrix_line = None

        for line in reversed(lines):
            if line.startswith('[[') and line.endswith(']]'):
                matrix_line = line
                break

        if matrix_line is None:
            text = prolog_str.strip()
            if text.startswith('[[') and text.endswith(']]'):
                matrix_line = text
            else:
                return None

        content = matrix_line.strip()

        if content.startswith('[['):
            content = content[2:]

        if content.endswith(']]'):
            content = content[:-2]

        row_strings = re.split(r'\],\s*\[', content)

        matrix = []

        for row_str in row_strings:
            row_str = row_str.replace('[', '').replace(']', '')
            cells = []

            for cell in row_str.split(','):
                cell = cell.strip().strip("'").strip('"')
                cells.append(cell)

            matrix.append(cells)

        return matrix

    except Exception as e:
        print(f"Error parsing Prolog matrix: {e}")
        return None


def parse_faces_from_write_result(output):
    """
    Parse output generated by Prolog write_result/6.

    Expected format:

    RESULT:
    FRONT:
    [[...]]
    BACK:
    [[...]]
    LEFT:
    [[...]]
    RIGHT:
    [[...]]
    UP:
    [[...]]
    DOWN:
    [[...]]
    """
    if 'RESULT:' not in output:
        return None

    lines = output.splitlines()
    faces_data = {}
    current_face = None
    matrix_lines = []

    for line in lines:
        line_stripped = line.strip()

        if line_stripped in ['FRONT:', 'BACK:', 'LEFT:', 'RIGHT:', 'UP:', 'DOWN:']:
            if current_face and matrix_lines:
                matrix_str = ''.join(matrix_lines)
                parsed = parse_prolog_matrix(matrix_str)

                if parsed:
                    faces_data[current_face] = parsed

            current_face = line_stripped[:-1].lower()
            matrix_lines = []

        elif line_stripped and line_stripped.startswith('[') and current_face:
            matrix_lines.append(line_stripped)

    if current_face and matrix_lines:
        matrix_str = ''.join(matrix_lines)
        parsed = parse_prolog_matrix(matrix_str)

        if parsed:
            faces_data[current_face] = parsed

    required = ['front', 'back', 'left', 'right', 'up', 'down']

    if all(face in faces_data for face in required):
        return faces_data

    return None


def run_temp_prolog_file(prolog_code, temp_filename):
    """Write Prolog code to a temporary file and execute it."""
    temp_query = os.path.join(PROLOG_DIR, temp_filename)

    try:
        with open(temp_query, 'w', encoding='utf-8') as f:
            f.write(prolog_code)

        result = subprocess.run(
            ['swipl', temp_query],
            capture_output=True,
            timeout=10,
            cwd=PROLOG_DIR
        )

        stdout = result.stdout.decode().strip()
        stderr = result.stderr.decode().strip()

        if stderr:
            print("Prolog stderr:")
            print(stderr)

        return stdout

    except subprocess.TimeoutExpired:
        print("Prolog timeout.")
        return ""

    except Exception as e:
        print(f"Error running Prolog temp file: {e}")
        return ""

    finally:
        try:
            os.remove(temp_query)
        except OSError:
            pass


