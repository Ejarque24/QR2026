def apply_rotation_via_prolog(faces, rotation_type):
    """
    Call Prolog to apply normal cube rotation.

    Uses:
        apply_rotation_via_query/13
    """
    try:
        front_str = matrix_to_prolog(faces['front'])
        back_str = matrix_to_prolog(faces['back'])
        left_str = matrix_to_prolog(faces['left'])
        right_str = matrix_to_prolog(faces['right'])
        up_str = matrix_to_prolog(faces['up'])
        down_str = matrix_to_prolog(faces['down'])

        prolog_code = f"""
:- consult('{ROTATION_PROLOG_FILE}').

:- apply_rotation_via_query(
    {front_str},
    {back_str},
    {left_str},
    {right_str},
    {up_str},
    {down_str},
    {rotation_type},
    NF, NB, NL, NR, NU, ND),
   write_result(NF, NB, NL, NR, NU, ND),
   halt.
"""

        output = run_temp_prolog_file(
            prolog_code,
            '.temp_rotation_qor.pl'
        )

        parsed_faces = parse_faces_from_write_result(output)

        if parsed_faces:
            return parsed_faces

        print(f"Failed to parse rotation output for {rotation_type}")
        print(output)
        return faces

    except Exception as e:
        print(f"Error calling Prolog for {rotation_type}: {e}")
        return faces


# ============================================================================
# IN-PLANE ROTATION CALL — ONLY 1q AND -1q
# ============================================================================

def apply_in_plane_rotation(faces, in_plane_rotation):
    """
    Call Prolog to apply full 6-face in-plane rotation.

    Only supports:
        '1q'
        '-1q'

    Uses existing Prolog predicate:
        apply_in_plane_rotation_full/13
    """
    try:
        rotation = str(in_plane_rotation)

        if rotation not in {'1q', '-1q'}:
            print(f"Ignoring unsupported in-plane rotation: {rotation}")
            return faces

        front_str = matrix_to_prolog(faces['front'])
        back_str = matrix_to_prolog(faces['back'])
        left_str = matrix_to_prolog(faces['left'])
        right_str = matrix_to_prolog(faces['right'])
        up_str = matrix_to_prolog(faces['up'])
        down_str = matrix_to_prolog(faces['down'])

        prolog_code = f"""
:- consult('{ROTATION_PROLOG_FILE}').

:- apply_in_plane_rotation_full(
    {front_str},
    {back_str},
    {left_str},
    {right_str},
    {up_str},
    {down_str},
    '{rotation}',
    NF, NB, NL, NR, NU, ND),
   write_result(NF, NB, NL, NR, NU, ND),
   halt.
"""

        output = run_temp_prolog_file(
            prolog_code,
            '.temp_in_plane_rotation_qor.pl'
        )

        parsed_faces = parse_faces_from_write_result(output)

        if parsed_faces:
            return parsed_faces

        print(f"Failed to parse in-plane rotation output for {rotation}")
        print(output)
        return faces

    except Exception as e:
        print(f"Error executing Prolog for in-plane rotation {in_plane_rotation}: {e}")
        return faces


