def render_objects_from_q3d(obj_names=None, output_dir=None):
    """Render static, normal rotations, and 1q/-1q in-plane rotations."""
    base = Path(__file__).parent

    if output_dir is None:
        output_dir = base / 'test_objects_qor'

    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    if obj_names is None:
        obj_names = [f'object{i}' for i in range(1, 8)]

    for obj in obj_names:
        print(f"Rendering {obj}...", end=' ', flush=True)

        faces = extract_faces_from_q3d(
            obj,
            q3d_path=base / Q3D_PROLOG_FILE
        )

        if not all(faces.values()):
            print('✗ missing faces')
            continue

        try:
            static_path = str(output_dir / f'{obj}_cube_qor.png')

            fig = draw_cube_grid(
                faces,
                f'{obj.upper()} - 3D Cube Views',
                static_path
            )

            plt.close(fig)

        except Exception as e:
            print(f"static failed: {e}")

        normal_rotations = [
            'towards_up',
            'towards_down',
            'towards_left',
            'towards_right'
        ]

        for rotation in normal_rotations:
            try:
                rotated_faces = apply_rotation_via_prolog(faces, rotation)

                save_path = str(
                    output_dir / f'{obj}_rotation_{rotation}_qor.png'
                )

                fig = draw_rotation_comparison(
                    faces,
                    rotated_faces,
                    rotation,
                    save_path
                )

                plt.close(fig)

            except Exception as e:
                print(f"{rotation} failed: {e}")

        in_plane_rotations = ['1q', '-1q']

        for rotation in in_plane_rotations:
            try:
                rotated_faces = apply_in_plane_rotation(faces, rotation)

                safe_rotation_name = rotation.replace('-', 'minus_')

                save_path = str(
                    output_dir / f'{obj}_in_plane_rotation_{safe_rotation_name}_qor.png'
                )

                fig = draw_rotation_comparison(
                    faces,
                    rotated_faces,
                    f'in-plane {rotation}',
                    save_path
                )

                plt.close(fig)

            except Exception as e:
                print(f"in-plane {rotation} failed: {e}")

        print('✓')


