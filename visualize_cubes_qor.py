def draw_cube_grid(faces, title='Cube Visualization', save_path=None):
    """
    Draw cube with 6 faces in 2D grid layout:

           [Up]
    [Left][Front][Right]
          [Down][Back]
    """
    fig, axes = plt.subplots(3, 3, figsize=(10, 10))
    fig.suptitle(title, fontsize=16, fontweight='bold')

    for row in axes:
        for ax in row:
            ax.set_visible(False)

    positions = {
        'up': (0, 1),
        'left': (1, 0),
        'front': (1, 1),
        'right': (1, 2),
        'down': (2, 1),
        'back': (2, 2),
    }

    for face_name, (row, col) in positions.items():
        if faces.get(face_name) is None:
            continue

        ax = axes[row, col]
        ax.set_visible(True)

        matrix = faces[face_name]
        rows = len(matrix)
        cols = len(matrix[0]) if matrix else 0

        ax.set_xlim(-0.5, cols - 0.5)
        ax.set_ylim(-0.5, rows - 0.5)
        ax.set_aspect('equal')
        ax.invert_yaxis()
        ax.set_xticks([])
        ax.set_yticks([])
        ax.set_title(face_name.upper(), fontweight='bold', fontsize=12)

        for r, row_data in enumerate(matrix):
            for c, value in enumerate(row_data):
                color = DEPTH_COLORS.get(value, '#FFFFFF')

                rect = Rectangle(
                    (c - 0.4, r - 0.4),
                    0.8,
                    0.8,
                    facecolor=color,
                    edgecolor='black',
                    linewidth=1.5
                )

                ax.add_patch(rect)

                ax.text(
                    c,
                    r,
                    value,
                    ha='center',
                    va='center',
                    fontsize=12,
                    fontweight='bold',
                    color='black'
                )

    legend_elements = [
        mpatches.Patch(
            facecolor=DEPTH_COLORS[key],
            edgecolor='black',
            label=DEPTH_LABELS[key]
        )
        for key in ['a', 'b', 'c', 'd', 'e', '*']
    ]

    fig.legend(
        handles=legend_elements,
        loc='upper center',
        bbox_to_anchor=(0.5, -0.02),
        ncol=6,
        fontsize=10
    )

    plt.tight_layout()

    if save_path:
        plt.savefig(save_path, dpi=150, bbox_inches='tight')
        print(f"Saved: {save_path}")

    return fig


